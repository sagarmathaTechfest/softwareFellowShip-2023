# To Uppercase 

a = "Hello, World!"
print(a.upper())

# To Lowercase

a = "Hello, World!"
print(a.lower())

# Remove Whitespace

a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

# Replace String

a = "Hello, World!"
print(a.replace("H", "J"))

# Split String

a = "Hello, World, Nepal, Kathmandu"
print(a.split("o")) # returns ['Hello', ' World!']

# String Concatenation

a = "Hello"
b = "World"
c = a + b
print(c)

a = "Hello"
b = "World"
c = a + " " + b
print(c)